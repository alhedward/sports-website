function apiUrl(path) {
  if (!API_BASE_URL) throw new Error('Missing API URL. Check config.js generated by Terraform.');
  return `${API_BASE_URL}${path}`;
}

async function fetchJson(path, fallback = null) {
  try {
    const url = path.startsWith('/api-placeholder') ? path.replace('/api-placeholder', '') : apiUrl(path);
    const response = await fetch(url);
    if (!response.ok) throw new Error(`request failed: ${response.status}`);
    return response.json();
  } catch (err) {
    if (fallback !== null) return fallback;
    throw err;
  }
}

function fmtDate(value) {
  if (!value) return 'TBA';
  return new Intl.DateTimeFormat(undefined, { dateStyle: 'medium' }).format(new Date(value));
}

function safe(text) {
  return String(text ?? '').replace(/[&<>'"]/g, char => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', "'": '&#39;', '"': '&quot;' }[char]));
}

function sourceLink(item, label = null) {
  const url = item.official_url || item.source_url;
  if (!url) return 'Not supplied';
  return `<a href="${safe(url)}" target="_blank" rel="noreferrer">${safe(label || item.cta_label || item.source_name || 'Official site')}</a>`;
}

