function shortRankLabel(label) {
  return String(label || 'Featured athlete')
    .replace(/\s*\/\s*/g, ' / ')
    .replace(/\bspotlight\b/ig, '')
    .replace(/\s{2,}/g, ' ')
    .trim() || 'Featured athlete';
}

