async function search() {
  const q = els.searchInput.value.trim();
  if (!q) {
    await loadHome();
    return;
  }
  const results = await fetchJson(`/search?q=${encodeURIComponent(q)}`);
  renderBodies(results.organisations || []);
  renderTournaments(results.tournaments || []);
  renderPathways(results.players || []);
  renderTopPlayers(results.top_players || []);
}

async function loadHome() {
  const [organisations, tournaments, players, topPlayers, events] = await Promise.all([
    fetchJson('/organisations'),
    fetchJson('/tournaments'),
    fetchJson('/players'),
    fetchJson('/top-players'),
    fetchJson('/events'),
  ]);
  els.bodyCount.textContent = organisations.length;
  els.tournamentCount.textContent = tournaments.length;
  els.playerCount.textContent = players.length;
  els.topPlayerCount.textContent = topPlayers.length;
  renderBodies(organisations);
  renderTournaments(tournaments);
  renderPathways(players.slice(0, 8));
  renderTopPlayers(topPlayers);
  await loadSponsors();
}




function syncStandaloneModeClass() {
  const query = window.matchMedia ? window.matchMedia('(display-mode: standalone)') : null;
  const standalone = Boolean(query?.matches || window.navigator.standalone === true);
  document.body.classList.toggle('standalone-mode', standalone);
}

function scrollToTop() {
  window.scrollTo({ top: 0, behavior: 'smooth' });
  history.replaceState(null, '', `${window.location.pathname}${window.location.search}`);
}

function syncDrawerToggleState(isOpen) {
  if (!els.drawerToggle) return;
  els.drawerToggle.setAttribute('aria-expanded', String(isOpen));
  els.drawerToggle.setAttribute('aria-label', isOpen ? 'Close section navigation' : 'Open section navigation');
  const glyph = els.drawerToggle.querySelector('.drawer-glyph');
  if (glyph) glyph.textContent = isOpen ? '×' : '☰';
}

function openDrawer() {
  if (!els.sectionDrawer || !els.drawerOverlay || !els.drawerToggle) return;
  els.sectionDrawer.classList.add('open');
  els.sectionDrawer.setAttribute('aria-hidden', 'false');
  els.drawerOverlay.hidden = false;
  document.body.classList.add('drawer-open');
  syncDrawerToggleState(true);
  els.drawerClose?.focus();
}

function closeDrawer() {
  if (!els.sectionDrawer || !els.drawerOverlay || !els.drawerToggle) return;
  els.sectionDrawer.classList.remove('open');
  els.sectionDrawer.setAttribute('aria-hidden', 'true');
  els.drawerOverlay.hidden = true;
  document.body.classList.remove('drawer-open');
  syncDrawerToggleState(false);
}

function toggleDrawer() {
  if (document.body.classList.contains('drawer-open')) {
    closeDrawer();
  } else {
    openDrawer();
  }
}

function updateBackToTopVisibility() {
  const show = window.scrollY > 560;
  if (els.backToTopButton) els.backToTopButton.hidden = !show;
}

function setupSectionNavigation() {
  els.drawerToggle?.addEventListener('click', toggleDrawer);
  els.drawerClose?.addEventListener('click', closeDrawer);
  els.drawerOverlay?.addEventListener('click', closeDrawer);
  els.drawerBackToTop?.addEventListener('click', () => {
    closeDrawer();
    scrollToTop();
  });
  els.backToTopButton?.addEventListener('click', scrollToTop);

  els.drawerLinks?.forEach(link => {
    link.addEventListener('click', event => {
      const href = link.getAttribute('href') || '';
      if (!href.startsWith('#')) return;
      event.preventDefault();
      closeDrawer();
      if (href === '#top') {
        scrollToTop();
        return;
      }
      const target = document.querySelector(href);
      if (target) {
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
        history.replaceState(null, '', `${window.location.pathname}${window.location.search}${href}`);
      }
    });
  });

  syncDrawerToggleState(document.body.classList.contains('drawer-open'));

  window.addEventListener('keydown', event => {
    if (event.key === 'Escape') closeDrawer();
  });
  window.addEventListener('scroll', updateBackToTopVisibility, { passive: true });
  updateBackToTopVisibility();

  const sectionIds = Array.from(els.drawerLinks || [])
    .map(link => (link.getAttribute('href') || '').replace('#', ''))
    .filter(id => id && id !== 'top');
  const sections = sectionIds.map(id => document.getElementById(id)).filter(Boolean);
  if ('IntersectionObserver' in window && sections.length) {
    const observer = new IntersectionObserver(entries => {
      const visible = entries
        .filter(entry => entry.isIntersecting)
        .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];
      if (!visible) return;
      els.drawerLinks.forEach(link => {
        link.classList.toggle('active', link.getAttribute('href') === `#${visible.target.id}`);
      });
    }, { rootMargin: '-30% 0px -55% 0px', threshold: [0.08, 0.22, 0.45] });
    sections.forEach(section => observer.observe(section));
  }
}

async function submitSuggestion(event) {
  event.preventDefault();
  const form = event.currentTarget;
  const payload = Object.fromEntries(new FormData(form).entries());
  els.suggestionStatus.textContent = 'Submitting suggestion…';
  try {
    const response = await fetch(apiUrl('/suggestions'), {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(payload),
    });
    const body = await response.json().catch(() => ({}));
    if (!response.ok) throw new Error(body.message || `Request failed: ${response.status}`);
    els.suggestionStatus.textContent = `Thanks — suggestion ${body.id} is pending review.`;
    form.reset();
  } catch (err) {
    els.suggestionStatus.textContent = `Could not submit suggestion: ${err.message}`;
  }
}

